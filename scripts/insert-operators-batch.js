async function main() {
  console.log("Iniciando inserção de operadores...")
  console.log(`Total de operadores: ${operators.length}`)

  // Buscar operadores existentes
  const { data: existingUsers, error: fetchError } = await supabase
    .from("users")
    .select("username, email")
    .eq("role", "operator")

  if (fetchError) {
    console.error("Erro ao buscar operadores existentes:", fetchError.message)
    process.exit(1)
  }

  const existingUsernames = new Set(existingUsers?.map((u) => u.username) || [])
  const existingEmails = new Set(existingUsers?.map((u) => u.email) || [])

  console.log(`Operadores existentes no banco: ${existingUsernames.size}`)

  // Filtrar operadores novos
  const newOperators = operators.filter((op) => {
    const email = `${op.username}@roteiro.com`
    return !existingUsernames.has(op.username) && !existingEmails.has(email)
  })

  console.log(`Novos operadores para inserir: ${newOperators.length}`)

  if (newOperators.length === 0) {
    console.log("Nenhum novo operador para inserir.")
    return
  }

  // Preparar dados para inserção em batch
  const operatorsToInsert = newOperators.map((op) => ({
    name: op.name,
    username: op.username,
    email: `${op.username}@roteiro.com`,
    role: "operator",
    is_active: true,
  }))

  // Inserir em batches de 50
  const batchSize = 50
  let inserted = 0

  for (let i = 0; i < operatorsToInsert.length; i += batchSize) {
    const batch = operatorsToInsert.slice(i, i + batchSize)
    const { error } = await supabase.from("users").insert(batch)

    if (error) {
      console.error(`Erro ao inserir batch ${i / batchSize + 1}:`, error.message)
      // Tentar inserir um por um se o batch falhar
      for (const op of batch) {
        const { error: singleError } = await supabase.from("users").insert(op)
        if (singleError) {
          console.error(`  Erro ao inserir ${op.username}: ${singleError.message}`)
        } else {
          inserted++
          console.log(`  Inserido: ${op.name}`)
        }
      }
    } else {
      inserted += batch.length
      console.log(`Batch ${i / batchSize + 1} inserido com sucesso (${batch.length} operadores)`)
    }
  }

  console.log(`\nTotal inserido: ${inserted} operadores`)
  console.log("Processo finalizado!")
}

