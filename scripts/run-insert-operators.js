async function insertOperators() {
  console.log(`Iniciando insercao de ${operators.length} operadores...`)
  
  let inserted = 0
  let skipped = 0
  let errors = 0

  for (const op of operators) {
    const email = `${op.username.toLowerCase()}@gruporoveri.com`
    
    // Verificar se ja existe
    const { data: existing } = await supabase
      .from("users")
      .select("id")
      .eq("email", email)
      .single()

    if (existing) {
      skipped++
      continue
    }

    // Inserir novo operador
    const { error } = await supabase.from("users").insert({
      name: op.name,
      email: email,
      role: "operator",
      is_active: true,
    })

    if (error) {
      console.error(`Erro ao inserir ${op.name}: ${error.message}`)
      errors++
    } else {
      inserted++
    }
  }

  console.log(`\nResultado:`)
  console.log(`- Inseridos: ${inserted}`)
  console.log(`- Ja existiam: ${skipped}`)
  console.log(`- Erros: ${errors}`)
  console.log(`- Total processados: ${operators.length}`)
}

